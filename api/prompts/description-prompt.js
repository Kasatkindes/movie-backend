/**
 * Build user message with movie data for description generation.
 * @param {{ title: string, plotContext: string, genre: string, mood: string }} opts
 * @returns {string}
 */
function buildDescriptionUserMessage(opts) {
  var title = (opts && opts.title != null) ? String(opts.title).trim() : '';
  var plotContext = (opts && opts.plotContext != null) ? String(opts.plotContext).trim() : '—';
  var genre = (opts && opts.genre != null) ? String(opts.genre).trim() : '—';
  var mood = (opts && opts.mood != null) ? String(opts.mood).trim() : '—';
  return 'Название фильма: ' + title + '\nКраткий нейтральный контекст сюжета: ' + plotContext + '\nЖанр: ' + genre + '\nНастроение: ' + mood + '\n\nНапиши описание по инструкции.';
}

